# polymorphism --> Method Overriding and Method Overloading

class Animal:
    def make_sound(self):
        print("Some animal sound")
    
class Dog(Animal):
    def make_sound(self):
        print("Dog says: Ghew Ghew")

class Cat(Animal):
    def make_sound(self):
        print("Cat says: Meow Meow")

class Cow(Animal):
    def make_sound(self):
        print("Cow says: Hamba Hamba")

animals = [Dog(), Cat(), Cow()]

for animal in animals:
    animal.make_sound()

#output:
# Dog says: Ghew Ghew
# Cat says: Meow Meow
# Cow says: Hamba Hamba

